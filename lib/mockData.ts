export function getTierForSales(totalSales: number): CommissionTier {
  for (let i = TIER_CONFIG.length - 1; i >= 0; i--) {
    if (totalSales >= TIER_CONFIG[i].minSales) {
      return TIER_CONFIG[i].name;
    }
  }
  return 'bronze';
}

export function getTierConfig(tier: CommissionTier): TierConfig {
  return TIER_CONFIG.find((t) => t.name === tier) || TIER_CONFIG[0];
}

